package com.chat.models;

public class usuario {

	private String nickname;
	private String mensaje;
	private String cambiara;
	private String paraquien;

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getMensaje() {
		return mensaje;
	}

	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}

	public String getCambiara() {
		return cambiara;
	}

	public void setCambiara(String cambiara) {
		this.cambiara = cambiara;
	}

	public String getParaquien() {
		return paraquien;
	}

	public void setParaquien(String paraquien) {
		this.paraquien = paraquien;
	}
	
	
	
	
	

}
